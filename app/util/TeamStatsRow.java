package util;

public class TeamStatsRow {
	
	public int scoredGoals;
	
	public int scoredGoalsAgainst;
	
	public int points;
	
	public int wonGames;
	
	public int lostGames;
	
	public int tieGames;
	
	public int getScoredGoals() {
		return scoredGoals;
	}

	public void setScoredGoals(int scoredGoals) {
		this.scoredGoals = scoredGoals;
	}

	public int getScoredGoalsAgainst() {
		return scoredGoalsAgainst;
	}

	public void setScoredGoalsAgainst(int scoredGoalsAgainst) {
		this.scoredGoalsAgainst = scoredGoalsAgainst;
	}

	public int getPoints() {
		return points;
	}

	public void setPoints(int points) {
		this.points = points;
	}

	public int getWonGames() {
		return wonGames;
	}

	public void setWonGames(int wonGames) {
		this.wonGames = wonGames;
	}

	public int getLostGames() {
		return lostGames;
	}

	public void setLostGames(int lostGames) {
		this.lostGames = lostGames;
	}

	public int getTieGames() {
		return tieGames;
	}

	public void setTieGames(int tieGames) {
		this.tieGames = tieGames;
	}

}
